var news = dailyNews;
let selectedOptionValue;
var currentNews = 0;

document.addEventListener('DOMContentLoaded', (_event) => {
    let selectElement = document.querySelector('nav select');
    let storedOption = localStorage.getItem('selectedOption');

    if (storedOption) {
        selectElement.value = storedOption;
    }
    
    if (window.location.pathname === '/index.html') {
        changeNewsContext();
    }
    if (window.location.pathname === '/pages/top.html') {
        updateTopNews();
        loadTopNews();
    }
    loadNews();
});

document.querySelector('nav select').addEventListener('change', function(event) {
    selectedOptionValue = event.target.value;
    localStorage.setItem('selectedOption', selectedOptionValue);
});

function updateTopNews(){
    selectedOptionValue = localStorage.getItem('selectedOption'); 
    switch (selectedOptionValue) {
        case 'daily':
            news = dailyNews;
            break;
        case 'weekly':
            news = weeklyNews;
            break;
        case 'monthly':
            news = monthlyNews;
            break;
        default :
            console.log('No news selected');
    }
}

function nextNews() {
    currentNews = (currentNews + 1)
    if (currentNews >= news.length) currentNews = 0;
    changeNewsContext();
}

function prevNews() {
    currentNews = (currentNews - 1)
    if (currentNews < 0) currentNews = news.length - 1;
    changeNewsContext();
}

function setBackground() {
    document.getElementById('background-img').style.backgroundImage = 'url(' + news[currentNews].image + ')';
}

function setTitle(){
    const title = document.getElementById('news-title');
    title.textContent = news[currentNews].title;

}

function setSubtitle(){
    const subtitle = document.getElementById('news-subtitle');
    subtitle.textContent = news[currentNews].subtitle;
}

function setImage(){
    const image = document.getElementsByClassName('main-image');
    image[0].src = news[currentNews].image;
}

function setBody(){
    const body = document.getElementById('news-body');
    body.textContent = news[currentNews].body;

}

function loadNewsByNumber(newsNumber) {
    currentNews = newsNumber;
    loadNews();
}

function loadTopNews() {
    const news1 = document.getElementById('news-img-1');
    const news2 = document.getElementById('news-img-2');
    const news3 = document.getElementById('news-img-3');
    const news4 = document.getElementById('news-img-4');
    news1.style.backgroundImage = 'url(' + news[0].image + ')';
    news2.style.backgroundImage = 'url(' + news[1].image + ')';
    news3.style.backgroundImage = 'url(' + news[2].image + ')';
    news4.style.backgroundImage = 'url(' + news[3].image + ')';
    document.getElementById('news-title-1').textContent = news[0].title;
    document.getElementById('news-title-2').textContent = news[1].title;
    document.getElementById('news-title-3').textContent = news[2].title;
    document.getElementById('news-title-4').textContent = news[3].title;
    loadThumbs();
}

function loadNews() {
    setImage();
    setBody();
    loadThumbs();
    const mainTitle = document.getElementById('main-title');
    const mainSubtitle = document.getElementById('main-subtitle');
    mainTitle.textContent = news[currentNews].title;
    mainSubtitle.textContent = news[currentNews].subtitle;
}

function changeNewsContext() {
    setBackground();
    setTitle();
    setSubtitle();
}

function loadThumbs(){
    document.getElementById('thumb-up').textContent = "👍 " + news[currentNews].thumbsUp;
    document.getElementById('thumb-down').textContent = "👎 " + news[currentNews].thumbsDown;
}

function thumbsUp() {
    changeThumbs(news, 'thumb-up', '👍 ');
}

function thumbsDown() {
    changeThumbs(news, 'thumb-down', '👎 ');
}

function changeThumbs(news, identifier, textContent) {
    if(news[currentNews].voted) return window.alert('Ya votaste esta noticia');
    else {
        if(identifier === 'thumb-down'){
            news[currentNews].thumbsDown += 1;
            document.getElementById(identifier).textContent = textContent + news[currentNews].thumbsDown;
        }
        else {
            news[currentNews].thumbsUp += 1;
            document.getElementById(identifier).textContent = textContent + news[currentNews].thumbsUp;
        }
        news[currentNews].voted = true;
    }
}
